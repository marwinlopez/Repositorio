package Modelo;


public class Bancos {
     private String idbanco;
    private String banco;

    public Bancos() {
        idbanco ="";
        banco ="";
    }

    public String getIdbanco() {
        return idbanco;
    }

    public void setIdbanco(String idbanco) {
        this.idbanco = idbanco;
    }

    public String getBanco() {
        return banco;
    }

    public void setBanco(String banco) {
        this.banco = banco;
    }

    
}
