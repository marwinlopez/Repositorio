package Modelo;

/**
 *
 * @author Luis
 */
public class UbicacionAba {
    String id="";
    String cp="";
    String col="";
    String pob="";
    String mpo="";
    String edo="";

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCp() {
        return cp;
    }

    public void setCp(String cp) {
        this.cp = cp;
    }

    public String getCol() {
        return col;
    }

    public void setCol(String col) {
        this.col = col;
    }

    public String getPob() {
        return pob;
    }

    public void setPob(String pob) {
        this.pob = pob;
    }

    public String getMpo() {
        return mpo;
    }

    public void setMpo(String mpo) {
        this.mpo = mpo;
    }

    public String getEdo() {
        return edo;
    }

    public void setEdo(String edo) {
        this.edo = edo;
    }
 
}
