package Modelo;

public class Mapfre {
    private int clave;
    private String descripcion;

    public Mapfre() {
        clave=0;
        descripcion="";
    }

    public int getClave() {
        return clave;
    }

    public void setClave(int clave) {
        this.clave = clave;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
   
    
}
