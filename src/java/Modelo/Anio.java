package Modelo;


public class Anio {
     private int sequence;
    private int año;

    public Anio() {
        sequence =0;
        año =0;
    }

    public int getSequence() {
        return sequence;
    }

    public void setSequence(int sequence) {
        this.sequence = sequence;
    }

    public int getAño() {
        return año;
    }

    public void setAño(int año) {
        this.año = año;
    }
}
